/**
 * Hook for managing language state and translations
 * @returns {Object} { language, setLanguage, t, languages }
 */
export function useLanguage() {
  const [language, setLanguageState] = useState(() => {
    if (typeof window !== 'undefined') {
      return localStorage.getItem('mise_language') || 'en';
    }
    return 'en';
  });

  // Persist language changes to localStorage
  useEffect(() => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('mise_language', language);
    }
  }, [language]);

  const setLanguage = (code) => {
    if (languages.some(lang => lang.code === code)) {
      setLanguageState(code);
    }
  };

  // Merge current language with English fallbacks
  const t = useMemo(() => {
    return { ...translations.en, ...(translations[language] || {}) };
  }, [language]);

  return {
    language,
    setLanguage,
    t,
    languages,
  };
}

